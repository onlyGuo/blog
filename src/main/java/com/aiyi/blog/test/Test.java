    public static String bytesToHexString(byte[] bArr) {
        if (bArr == null) {
            return null;
        }
        StringBuffer sb = new StringBuffer(bArr.length);
        String sTmp;

        for (int i = 0; i < bArr.length; i++) {
            sTmp = Integer.toHexString(0xFF & bArr[i]);
            sb.append("0x");
            if (sTmp.length() < 2){
                sb.append(0);
            }
            sb.append(sTmp);
            sb.append(", ");
        }

        return sb.toString();
    }

